package parcial1;

public class Dato {
	private String letra; 
	private int numero;
	
	public Dato(String letra, int numero) {
		super();
		this.letra = letra;
		this.numero = numero;
	}

	public String getLetra() {
		return letra;
	}

	public void setLetra(String letra) {
		this.letra = letra;
	}

	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	} 
	
	
	
}
